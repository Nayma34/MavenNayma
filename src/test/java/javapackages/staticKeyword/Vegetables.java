package javapackages.staticKeyword;

public class Vegetables {
    //class- vegetables--- name, color, size, vitamin, nutration, price-----properties
    //objects- tomato, cucumber, potato, carrots



    String name;
    String color;
    String size;
    char vitamin;

    public static void main(String[] args) {

      Vegetables tomato= new Vegetables ();

      tomato. name= "Tomato";
      tomato. color= "Red";
      tomato.size= " Small";
      tomato.vitamin= 'A';

      Vegetables cucumber= new Vegetables();
      cucumber. name= "Cucumber";
      cucumber. color= "Green";
      cucumber.size= "Large";
      cucumber.vitamin='C';

      Vegetables potato= new Vegetables();
      potato.name= "Potato";
      potato.color="White";
      potato.size="Small";
      potato.vitamin='B';




    }








}
